package com.mycompany.stringmayusminus;

public class StringMayusMinus {
// EJERCICIO 4: Crea un String y muéstralo por pantalla en mayúsculas y en minúsculas.

    public static void main(String[] args) {

        String a = "Esto es un string de prueba";
        System.out.println(a.toUpperCase()); // Muestra el String "a" en mayúsculas.
        System.out.println(a.toLowerCase()); // Muestra el String "a" en minúsculas.

    }
}
